// Variable naming conventions & rules
// use descriptive names for variables - to achieve a clean and clear code
// use camelCase to write variable names
// other ways of writing variable names: snake_case but not recommended
// var names should not start with a number -> would return a SyntaxError
// var names can only contain letters, numbers, underscores or $ sign
// reserved keywords can't be the name of var (e.g. new', 'name' or 'function')
  // if you want to use these reserved words you can rename to $function or _new
// should not start the var name with uppercase
// var names in all caps are reserved for constants that will never change (e.g. PI)
